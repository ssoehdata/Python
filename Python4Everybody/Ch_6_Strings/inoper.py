# using 'in' as a logical operator ('in' used in a for loop functions differently)

# 'in' operator is a logical expression that returns True or False and 
# can be used in an 'if' statement.

# the 'in' keyword can be used to check if one string 
# is 'in' another (example below returns "True" then "False" respectively)
fruit = 'banana'
print('n' in fruit)
print('m' in fruit)


if 'a' in fruit:
    print('Found it!')
