# newline character 
# indicates when a line ends
# \n

# n.b.: 'print' adds an additional \n at the end. (you can override this behaviour)
stuff = 'Hello\nWorld!'
print(stuff)

stuff2 = 'X\nY'
print(stuff2)
print(len(stuff))
print(len(stuff2))  # this gives 3 characters because the \n is also a character


